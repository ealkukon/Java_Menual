package Funtionality;

public class Concatenation {

	public static void main(String[] args) {
		//addition Concatenation
		
		String s6="Hello";
		String s7="world";
		int i6=10;
		int i7=40;
		System.out.println(i6+i7+s6+s7);
		System.out.println(s6+i6+i7+s7);
		System.out.println(s6+(i6+i7)+s7);   

	}

}
