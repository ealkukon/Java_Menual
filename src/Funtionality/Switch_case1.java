package Funtionality;

public class Switch_case1 {

	public static void main(String[] args) {
		
		String month="march";
		switch(month) {
		case "jan":
			System.out.println("new student");
			break;
		case "feb":
			System.out.println("mid student");
			break;
		case "march":
			System.out.println("first student");

	}
	}
}
